def translate_to_braille(text):
    """
    Translates a given text into its Braille equivalent.
    
    Parameters:
        text (str): The input text to be translated.
        
    Returns:
        str: The translated Braille text.
    """
    braille_text = ''
    for char in text:
        braille_char = BRAILLE_DICT.get(char, '?')  # Use '?' for characters not found in the dictionary
        braille_text += braille_char
    return braille_text
